dir=/home/jer/h0t_d0g
i2c_dir=/usr/sbin

led_bus=1
led_addr=0x40
arg=$1

if [ -z "$*" ]
then
	echo "INVALID ARGUMENT"
	exit 0
elif [ $arg = "init" ]
then 
	python $dir/gpio.py leds_off
	addr=("0x00" "0x01" "0x12" "0x13" "0x14" "0x15" "0x16")
	data=("0x00" "0x21" "0x7f" "0x18" "0xaa" "0xaa" "0xaa")
elif [ $arg = "all_off" ]
then
	python $dir/gpio.py leds_off
	addr=("0x14" "0x15" "0x16" "0x02" "0x03" "0x04" "0x05" "0x06" "0x07" "0x08" "0x09" "0x0a" "0x0b" "0x0c" "0x0d")
	data=("0xaa" "0xaa" "0xaa" "0x00" "0x00" "0x00" "0x00" "0x00" "0x00" "0x00" "0x00" "0x00" "0x00" "0x00" "0x00")
elif [ $arg = "all_yellow" ]
then
	addr=("0x02" "0x03" "0x06" "0x07" "0x0a" "0x0b")
	data=("0x04" "0x02" "0x04" "0x02" "0x04" "0x02")
	python $dir/gpio.py leds_on
elif [ $arg = "blink_yellow" ]
then
	addr=("0x14" "0x15" "0x16" "0x02" "0x03" "0x06" "0x07" "0x0a" "0x0b")
	data=("0xff" "0xff" "0xff" "0x04" "0x02" "0x04" "0x02" "0x04" "0x02")
	python $dir/gpio.py leds_on
elif [ $arg = "all_green" ]
then
	addr=("0x03" "0x07" "0x0b")
	data=("0x02" "0x02" "0x02")
	python $dir/gpio.py leds_on
elif [ $arg = "all_red" ]
then
	addr=("0x02" "0x06" "0x0a")
	data=("0x04" "0x04" "0x04")
	python $dir/gpio.py leds_on
else
	echo "INVALID ARGUMENT"
	exit 0
fi

for i in ${!addr[@]}
do
	$i2c_dir/i2cset -y $led_bus $led_addr ${addr[$i]} ${data[$i]}
done
